 <p style="max-width: 100%;"> There are some cons to land-lease agreements, however. A lender might not want to give you a mortgage on an unsubordinated lease if they are going to have fewer rights to recover their investment than the landowner.</p>

 <p style="max-width: 100%;">You also might not know whether there are environmental issues with the land, or where the potential property encroachments are, as an example.</p>

