                            <p style="max-width: 100%;">The two main types of land leases are subordinated and unsubordinated, although unsubordinated leases are much more common because they provide more rights to the owner of the land.</p>
                            
                            <p style="max-width: 100%;">
                                In an unsubordinated lease, the property owner retains the top priority of claims on the land even if the tenant defaults on a loan for improvements. This might make it more difficult for someone to obtain mortgage financing to build or improve on a home on that land.</p>
                                
                                <p style="max-width: 100%;">
In a subordinated lease, however, the property owner takes a lower position for claims on the property if the tenant defaults on the mortgage, which can put the home equity at risk. The landowner can also increase the rental rate to compensate for the risk in this scenario.
